#include <stdio.h>
#include <stdlib.h>
#include <string.h>
#include <assert.h>
#include "mshell.h"
#include "portab.h"
#include "mme.h"
#include "rbt.h"
#include "lpstore.h"

/* A specification for the LP file format can be found in the
 * ILOG CPLEX 7.0 Reference Manual page 527.
 */
void lpf_write(
   const Lps* lp,
   FILE*      fp)
{
   const Var* var;
   const Con* con;
   const Nzo* nzo;
   Bool  first;
   int   cnt;
   char  tmp[MPS_NAME_LEN + 1];
   
   assert(lp     != NULL);
   assert(fp     != NULL);
   
   fprintf(fp, "\\This file was automatically generated by zimpl\n");
   fprintf(fp, "\\Problem name: %s\n", lp->name);   
   fprintf(fp, "%s\n", (lp->direct == LP_MIN) ? "Minimize" : "Maximize");
   fprintf(fp, " %s: ", lp->objname == NULL ? "Objective" : lp->objname);
   
   for(var = lp->var_root, cnt = 0; var != NULL; var = var->next)
   {
      if ((var->size > 0) && (fabs(var->cost) > EPS_ZERO))
      {
         lps_makename(tmp, MPS_NAME_LEN + 1, var->name, var->number);

         if (EQ(var->cost, 1.0))
            fprintf(fp, " + %s", tmp);
         else
            fprintf(fp, " %+.16g %s", var->cost, tmp);

         if (++cnt % 6 == 0)
            fprintf(fp, "\n ");
      }
   }  
   fprintf(fp, "\nSubject to\n");

   for(con = lp->con_root; con != NULL; con = con->next)
   {
      if (con->size == 0)
         continue;
      
      lps_makename(tmp, MPS_NAME_LEN + 1, con->name, con->number);
      fprintf(fp, " %s: ", tmp);

      for(nzo = con->first, cnt = 0; nzo != NULL; nzo = nzo->con_next)
      {
         lps_makename(tmp, MPS_NAME_LEN + 1, nzo->var->name, nzo->var->number);

         if (EQ(nzo->value, 1.0))
            fprintf(fp, " + %s", tmp);
         else
            fprintf(fp, " %+.16g %s", nzo->value, tmp);

         if (++cnt % 6 == 0)
            fprintf(fp, "\n ");         
      }
      switch(con->type)
      {
         case CON_LE :
            fprintf(fp, " <= ");
            break;
         case CON_GE :
            fprintf(fp, " >= ");
            break;
         case CON_EQ :
            fprintf(fp, " = ");
            break;
         default :
            abort();
      }
      fprintf(fp, "%.16g\n", con->rhs);
   }
   fprintf(fp, "Bounds\n");

   for(var = lp->var_root; var != NULL; var = var->next)
   {
      if (var->size > 0)
      {
         if (LE(var->lower, -INFINITY))
            fprintf(fp, " -Inf");
         else
            fprintf(fp, " %.16g", var->lower);

         lps_makename(tmp, MPS_NAME_LEN + 1, var->name, var->number);
         fprintf(fp, " <= %s <= ", tmp);
         
         if (GE(var->upper, INFINITY))
            fprintf(fp, "+Inf\n");
         else
            fprintf(fp, "%.16g\n", var->upper);
      }
   }
   for(first = TRUE, var = lp->var_root; var != NULL; var = var->next)
   {
      if ((var->size > 0) && (var->type == VAR_INT))
      {
         if (first)
         {
            fprintf(fp, "General\n");
            first = FALSE;
         }
         lps_makename(tmp, MPS_NAME_LEN + 1, var->name, var->number);
         fprintf(fp, " %s\n", tmp);
      }
   }
   for(first = TRUE, var = lp->var_root; var != NULL; var = var->next)
   {
      if ((var->size > 0) && (var->type == VAR_BIN))
      {
         if (first)
         {
            fprintf(fp, "Binary\n");
            first = FALSE;
         }
         lps_makename(tmp, MPS_NAME_LEN + 1, var->name, var->number);
         fprintf(fp, " %s\n", tmp);
      }
   }
   fprintf(fp, "End\n");
}   



